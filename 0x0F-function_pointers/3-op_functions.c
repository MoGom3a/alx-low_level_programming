#include "3-calc.h"

/**
 * op_add - for adding
 * @a: num1
 * @b: num2
 *
 * Return: the result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - for adding
 * @a: num1
 * @b: num2
 *
 * Return: the result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - for adding
 * @a: num1
 * @b: num2
 *
 * Return: the result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - for adding
 * @a: num1
 * @b: num2
 *
 * Return: the result
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - for adding
 * @a: num1
 * @b: num2
 *
 * Return: the result
 */

int op_mod(int a, int b)
{
	return (a % b);
}

